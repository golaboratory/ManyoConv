// ConvertManyoToKana 万葉仮名から仮名に変換する
func ConvertManyoToKana(s string) string {
	var result = s
	for _, c := range s {
		for i, m := range manyoList {
			if strings.IndexRune(m, c) != -1 {
				result = strings.Replace(result, m, kanaList[i], -1)
				break
			}
		}
	}
	return result
}
